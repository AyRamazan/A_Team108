package Days02_Variables_scanner;

public class C03_NonPrimitiveDataTypes {

    public static void main(String[] args) {

        String str= "Java candir";
        int sayi= 10;

        System.out.println(sayi);

        System.out.println(str.toLowerCase());// java candir

        System.out.println(str);

        System.out.println(str.toUpperCase());// JAVA CANDİR

        /*
        Primitive data turlerinin hazir methodlari yoktur
        ancak non-primitive data turlerinin onceden hazirlanmiş
        bizim kullanabileceğimiz method'lari olur

         */




    }
}
